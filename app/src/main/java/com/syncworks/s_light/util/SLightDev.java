public class SLightDev {
    private String name;
    private String addr;

    public SLightDev(String name, String addr) {
        this.name = name;
        this.addr = addr;
    }

    public String getName() {
        return this.name;
    }
    public String getAddr() {
        return this.addr;
    }
}
